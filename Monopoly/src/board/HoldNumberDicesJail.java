package board;

public class HoldNumberDicesJail {

	private int a,b;
	
	public HoldNumberDicesJail() {
		a = 0;
		b = 0;
	
	}
	
	public void setNumberJail(int d1, int d2) {
		a=d1;
		b=d2;
	}
	public int getNumberJailA() {
		return a;
	}
	public int getNumberJailB() {
		return b;
	}
	
}
